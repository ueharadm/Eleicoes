    private void buttonConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonConfirmarActionPerformed
        try{
            if(textSenha.getText().equals(textConfirmSenha.getText()) && !textSenha.getText().equals("")){
                Eleicoes.geleitor.listEleitor.add(new Eleitor(textNome.getText(), textCpf.getText(), textCidade.getText(), textUsername.getText(), textSenha.getText(), false));
                JOptionPane.showMessageDialog(this, "Eleitor cadastrado com sucesso.\n");
                Eleicoes.exportCSVEleitor("listEleitor.csv", Eleicoes.geleitor.listEleitor);
            }else if(textSenha.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Digite uma senha");
            }else{
                JOptionPane.showMessageDialog(this, "As senhas n√£o coincidem");
            }
        }catch(Exception e){
            System.err.println("Erro: "+e);
        }
        
        
    }//GEN-LAST:event_buttonConfirmarActionPerformed

